function dur = findClipDuration(audioclip)
	% audioclip = 'C:\Users\eoin\Music\boz48_stereo_24kbps_aac.wav';
	% cmd = ['C:\MediaInfoCLI\MediaInfo.exe --fullscan ' audioclip];
	% cmd = ['MediaInfo --fullscan ' audioclip];
	cmd = ['ffmpeg -i ' audioclip];
	console_output_text = evalc('dos(cmd)');
	durationMarker = 'Duration: ';
	durationInfoPos = strfind(console_output_text, durationMarker);
	durationInfoLong = console_output_text(durationInfoPos(1):durationInfoPos(1)+30);
	commapos = strfind(durationInfoLong, ',');
	startpos = length(durationMarker) + 1;
	endpos = commapos(1) - 1;
	durationString = durationInfoLong(startpos:endpos);
	colonpos = strfind(durationString, ':');
	hours = str2num(durationString(1:colonpos(1)-1));
	hours = hours * 60 * 60;
	minutes = str2num(durationString(colonpos(1)+1:colonpos(2)-1));
	minutes = minutes * 60;
	seconds = str2num(durationString(colonpos(2)+1:end));
	dur = hours + minutes + seconds;
